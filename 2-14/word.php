基本用語

1. 要件定義(要求仕様書)
クライアント側からの要求事項を取りまとめたもの

2. 単体テスト・結合テスト
単体テスト・・・コンピュータプログラミングにおいて単体テストあるいはユニットテストとは、ソースコードの個々のユニット、すなわち、1つ以上のコンピュータプログラムモジュールが使用に適しているかどうかを決定するために、関連する制御データ、使用手順、操作手順とともにテストする手法
結合テスト・・・統合テスト は、個々のソフトウェアモジュールを組み合わせて集合体としてテストするソフトウェアテストのフェーズ

3. テスト仕様書(どのようなもの、項目)
テスト仕様書とは、システムやソフトウェアが、クライアントのヒアリングをもとに作り上げた要件定義書の通りに機能するかどうか、テストするポイントをまとめたドキュメント。項目としては下記の通り
・テストを実施した環境
・実施するテストの内容
・テストを実施するためのシステムの操作手順
・テストの実行結果
・個々のテスト項目を識別するための番号や記号（通し番号など）
・テストを実施した年月日
・テストを実行した担当者