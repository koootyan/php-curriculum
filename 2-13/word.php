基本用語

1. PostgreSQL・Oracle SQL
PostgreSQL・・・拡張性とSQL準拠を強調するフリーでオープンソースの関係データベース管理システム
Oracle SQL・・・米国オラクルが開発・販売している関係データベース管理システム

2. TortoiseGit・TortoiseSVN
TortoiseGit・・・分散型バージョン管理システムであるGitのクライアントで、TortoiseSVNをもとに、 Microsoft Windows シェル拡張 として実装されている
TortoiseSVN・・・バージョン管理システムであるSubversionのクライアントフロントエンド

3. 外部設計・内部設計
外部設計・・・操作画面や操作方法、データ出力など、ユーザーから見えるインターフェース部分の仕様を決定したり、セキュリティや運用規定、システム開発のスケジュールや費用などを設計したりと、基本的にユーザーに向けた仕様を設計するもの
内部設計・・・外部設計を基に、システム内部の動作や機能、物理データなど、ユーザーから見えにくい詳細な部分の設計を行うもの